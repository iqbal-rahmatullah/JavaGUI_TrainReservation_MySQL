public class Customer {
    private int id;
    private String nama;
    private String no_ktp;
    private String no_hp;

    /**
     * getter id customer
     *
     * @return id customer
     */
    public int getId() {
        return id;
    }

    /**
     * Setter id customer
     *
     * @param id
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * Getter nama customer
     *
     * @return nama customer
     */
    public String getNama() {
        return nama;
    }

    /**
     * Setter nama customer
     *
     * @param nama
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * Getter no ktp customer
     *
     * @return no ktp customer
     */
    public String getNo_ktp() {
        return no_ktp;
    }

    /**
     * Setter no ktp customer
     *
     * @param no_ktp
     */
    public void setNo_ktp(String no_ktp) {
        this.no_ktp = no_ktp;
    }

    /**
     * Getter no hp customer
     *
     * @return no hp customer
     */
    public String getNo_hp() {
        return no_hp;
    }

    /**
     * Setter no hp customer
     *
     * @param no_hp
     */
    public void setNo_hp(String no_hp) {
        this.no_hp = no_hp;
    }
    
    
}
